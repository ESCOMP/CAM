         b(30) = b(30) * lu(34)
         b(29) = b(29) * lu(33)
         b(28) = b(28) * lu(32)
         b(27) = b(27) * lu(31)
         b(26) = b(26) * lu(30)
         b(25) = b(25) * lu(29)
         b(24) = b(24) * lu(28)
         b(23) = b(23) * lu(27)
         b(22) = b(22) * lu(26)
         b(21) = b(21) * lu(25)
         b(20) = b(20) * lu(24)
         b(19) = b(19) * lu(23)
         b(18) = b(18) * lu(22)
